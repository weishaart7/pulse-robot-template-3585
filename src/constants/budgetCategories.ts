// Fonction utilitaire pour obtenir toutes les natures d'une catégorie donnée
export const getNaturesByCategory = (categories: typeof REVENUS_CATEGORIES | typeof CHARGES_CATEGORIES, category: string): string[] => {
  return categories[category as keyof typeof categories] || [];
};

// Fonction utilitaire pour rechercher dans toutes les natures
export const searchInAllNatures = (categories: typeof REVENUS_CATEGORIES | typeof CHARGES_CATEGORIES, searchTerm: string): string[] => {
  const allNatures: string[] = [];
  Object.values(categories).forEach(categoryNatures => {
    allNatures.push(...categoryNatures);
  });
  
  return allNatures.filter(nature => 
    nature.toLowerCase().includes(searchTerm.toLowerCase())
  );
};